# aprendo a lidar com variáveis e a impresão delas.

nome = 'Gabriel'
idade = '36'


# este tipo de impressão é mais trabalhosa e menos produtiva:
print('Meu nome é ' + nome + '.' + ' Eu tenho ' + idade + ' anos.')

# o ideal seria usar FORMATED STRINGS assim:
print(f'Meu nome é {nome}. Eu tenho {idade} anos.')
